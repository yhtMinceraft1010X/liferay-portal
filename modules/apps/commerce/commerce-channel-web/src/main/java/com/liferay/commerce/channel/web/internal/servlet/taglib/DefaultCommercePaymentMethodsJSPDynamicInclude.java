package com.liferay.commerce.channel.web.internal.servlet.taglib;

import com.liferay.commerce.payment.method.CommercePaymentMethodRegistry;
import com.liferay.portal.kernel.log.Log;
import com.liferay.portal.kernel.log.LogFactoryUtil;
import com.liferay.portal.kernel.servlet.taglib.BaseJSPDynamicInclude;
import com.liferay.portal.kernel.servlet.taglib.DynamicInclude;

import java.io.IOException;

import javax.servlet.ServletContext;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.osgi.service.component.annotations.Component;
import org.osgi.service.component.annotations.Reference;

/**
 * @author Andrea Sbarra
 */
@Component(enabled = false, immediate = true, service = DynamicInclude.class)
public class DefaultCommercePaymentMethodsJSPDynamicInclude
	extends BaseJSPDynamicInclude {

	@Override
	public void include(
			HttpServletRequest httpServletRequest,
			HttpServletResponse httpServletResponse, String key)
		throws IOException {

		httpServletRequest.setAttribute(
			"CommercePaymentMethodRegistry", _commercePaymentMethodRegistry);

		super.include(httpServletRequest, httpServletResponse, key);
	}

	@Override
	public void register(DynamicIncludeRegistry dynamicIncludeRegistry) {
		dynamicIncludeRegistry.register(
			"com.liferay.account.admin.web#/account_entries_admin" +
				"/account_entry/details.jsp#payment_methods");
	}

	@Override
	protected String getJspPath() {
		return "/dynamic_include/default_commerce_payment_methods.jsp";
	}

	@Override
	protected Log getLog() {
		return _log;
	}

	@Override
	@Reference(
		target = "(osgi.web.symbolicname=com.liferay.commerce.channel.web)",
		unbind = "-"
	)
	protected void setServletContext(ServletContext servletContext) {
		super.setServletContext(servletContext);
	}

	private static final Log _log = LogFactoryUtil.getLog(
		DefaultCommercePaymentMethodsJSPDynamicInclude.class);

	@Reference
	private CommercePaymentMethodRegistry _commercePaymentMethodRegistry;

}